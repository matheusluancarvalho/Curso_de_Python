"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro das aspas
"""

print(1, 'Matheus "Carvalho"', sep=' - ')

#=====================================Aspas simples=====================================

print(2, 'Matheus Carvalho', sep=' - ')

#=====================================Aspas duplas=====================================

print(3, "Matheus Carvalho", sep=' - ')

#=======================================Escape=======================================

print(4, "Matheus \"Carvalho\"", sep=' - ')
print(5, "Matheus \"Carvalho\"", sep=' - ')

#==========================================r==========================================

print(r"Matheus \"Carvalho\"")