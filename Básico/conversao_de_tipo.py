"""
Conversão de tipos, coersão, type convertion, typecasting, coercion é o ato de
converter um tipo em outros tipos imutáveis e primitivos: str, int, float e bool

"""
print(1 + 1)  # Soma
print('a' + 'b')  # Concatena

print('1', type('1'))
print(int('1'), type(int('1')))

print(int('1') + 1)
print(float('1') + 1)