"""
int -> Número inteiro
O tipo int representa qualquer número
seja ele positivo ou negativo

float -> Número com ponto flutuante
O tipo float reprsenta qualquer número
positivos e negativos fluatuantes

Função type mostra o tipo que o Python
inferiu ao valor

"""
#=================================================Tipo int======================================

print(1)
print(-1)
print(0)

#================================================Tipo float======================================

print(1.1)
print(-1.1)

#================================================Função type======================================

print(type(11))
print(type(-11))
print(type(11.2))
print(type(-11.2))
